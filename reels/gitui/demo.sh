printf '│ \033[38;5;214m?\033[0m reels/gitui/demo.sh               │                                        │\n'
printf '│ \033[38;5;214m?\033[0m reels/yazi/demo.sh                │                                        │\n'
printf '│                                       │                                        │\n'
printf '└───────────────────────────────────────┴────────────────────────────────────────┘\n'
printf '┌ Diff ───────────────────────────────────────────────────────────────────────────┐\n'
printf '│ @@ -41,6 +41,9 @@ func renderReadme(...)                                       │\n'
