#!/usr/bin/env bash
# gitui — git GUI comfort in terminal
#
# Template 1 (scripted fake). We show the core proposition in one loop:
# status + staged files + commit box + context key hints.

pause() { sleep "${1:-0.8}"; }
beat()  { sleep "${1:-0.3}"; }

type_line() {
  local s="$1" i
  for (( i=0; i<${#s}; i++ )); do
    printf '%s' "${s:$i:1}"
    sleep 0.03
  done
  printf '\n'
}

prompt() { printf '\033[2m$\033[0m '; }

clear
pause 0.6

prompt; type_line 'gitui'
beat
pause 0.3
clear

